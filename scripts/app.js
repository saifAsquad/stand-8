function showPoint(loc) {
	var rect = loc.actualBounds;
	rect.grow(rect.height / 6, rect.width / 6, rect.height / 6, rect.width / 6); // Copy and modify the new viewport rect so the new group takes up ~2/3rds of the screen
	var width = rect.width,
		height = rect.height,
		viewWidth = myDiagram.viewportBounds.width,
		viewHeight = myDiagram.viewportBounds.height;

	var scale = Math.min(
		(viewWidth * myDiagram.scale) / rect.width,
		(viewHeight * myDiagram.scale) / rect.height
	); // Sanity check scale value

	if (viewHeight / height > viewWidth / width) {
		scale = viewHeight / height; // Copied from zoomToRect; check if the height or the width is the 'limiting factor' and scale from there.
	} else {
		scale = viewWidth / width;
	}
	// Build an animation from the current to the newly calc'd values and animate!
	var animation = new go.Animation();
	animation.add(myDiagram, "scale", myDiagram.scale, scale / 6);
	animation.add(
		myDiagram,
		"position",
		myDiagram.position,
		new go.Point(rect.x, rect.y)
	);
	animation.start();
}

const init = () => {
	const pushed = [
		{ key: 0, name: "Departments", color: "#ffd000" },
		...departments.map((item) => {
			return {
				key: item.id,
				color: item.color,
				name: item.departmentName,
			};
		}),
	];

	const links2 = [
		...departments.map((i) => {
			return {
				from: 0,
				to: i.id,
			};
		}),
	];

	// Fill the departments array
	const fillDepartments = () => {
		departments.forEach((e) => {
			let lastIndex;
			e.users.forEach((item, index) => {
				pushed.push({
					key: item.id,
					name: item.name,
					role: item.role,
					color: e.color,
					avatar: item.avatar,
				});
				links2.push({
					from: index === 0 ? e.id : e.users[lastIndex].id,
					to: item.id,
				});
				lastIndex = index;
			});
		});
	};
	fillDepartments();

	// define a simple Node template
	myDiagram.nodeTemplate = $(
		go.Node,
		"Auto", // the Shape will go around the TextBlock
		{ selectionAdorned: false },
		$(
			go.Shape,
			"roundedRectangle",
			{
				strokeWidth: 0,
				minSize: new go.Size(189, 45),
				height: 45,
				fill: "white",
			},
			// Shape.fill is bound to Node.data.color
			new go.Binding("fill", function (n) {
				if (departments.some((item) => item.id === n.data.key)) {
					return departments.find((item) => item.id === n.data.key).color;
				} else {
					return "#fff";
				}
			}).ofObject()
		),
		$(
			go.TextBlock,

			{
				editable: true,
				font: "bold 14px serif",
				stroke: "#334155",
			}, // some room around the text
			// TextBlock.text is bound to Node.data.key
			new go.Binding("text", "name"),
			new go.Binding("margin", function (n) {
				if (
					departments.some((item) =>
						item.users.some((i) => i.id === n.data.key)
					)
				) {
					return new go.Margin(0, 20, 18, 0);
				}
			}).ofObject()
		),
		$(
			go.TextBlock,
			{
				editable: true,
				font: " 11px serif",
				stroke: "#64748B",
			}, // some room around the text
			// TextBlock.text is bound to Node.data.key
			new go.Binding("text", "role"),
			new go.Binding("margin", function (n) {
				if (
					departments.some((item) =>
						item.users.some((i) => i.id === n.data.key)
					)
				) {
					return new go.Margin(15, 0, 0, -55);
				}
			}).ofObject()
		),
		$(
			go.Panel,
			"Spot",
			{ isClipping: true, scale: 1, margin: new go.Margin(0, 185, 0, 0) },
			$(go.Shape, "Circle", { width: 37, strokeWidth: 0 }),
			$(
				go.Picture,
				{ width: 37, height: 37 },
				new go.Binding("source", "avatar")
			)
		),
		{
			// show the Adornment when a mouseHover event occurs
			mouseHover: (e, obj) => {
				const some = links2.some((item) => {
					if (obj.part.data.key === 0) {
						return true;
					}
					if (item.from === 0) {
						return item.to === obj.part.data.key;
					}
				});
				if (some) {
					var node = obj.part;
					nodeHoverAdornment.adornedObject = node;
					node.addAdornment("mouseHover", nodeHoverAdornment);
				}
			},
			click: function (e, obj) {
				showPoint(obj);
			},
		}
	);
	myDiagram.commandHandler.increaseZoom();
	myDiagram.model = new go.GraphLinksModel(pushed, links2);
};

